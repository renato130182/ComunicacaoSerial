    private void JTFAutorizacaoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JTFAutorizacaoKeyPressed
        // TODO add your handling code here:
        if(JTFAutorizacao.getText().length()==10){
            if(ControllerUtil.SoTemNumeros(JTFAutorizacao.getText())){
                String code = JTFAutorizacao.getText();
                JTFAutorizacao.setText("");                                                                            
                login.setCode(code);              
                if(login.logarCode(login)){
                    if(login.getNivel().equals("0")){
                        ControllerProducao prd = new ControllerProducao();
                        if(prd.atualizaMetragemProduzida(listaPesagens,Double.valueOf(JTFMetragem.getText()), codMaquina,maqPronta,jButtonFuncao.getText())){
                            System.out.println("Atualizar metragem produzida: " + JTFMetragem.getText());
                            ControllerEventosSistema ctr = new ControllerEventosSistema();
                            ctr.registraEventos(14,login.getCodigoOperador(),0,Integer.valueOf(JTFMetragem.getText()),codMaquina,lote);
                            doClose(RET_OK);
                        }else{
                            JOptionPane.showMessageDialog(null, "Falha ao atualizar Metragem","Controller Produção",JOptionPane.ERROR_MESSAGE);
                        }
                    }else{                        
                        JOptionPane.showMessageDialog(null, "Codigo de Identificação não autorizado","Codigo sem permisão",JOptionPane.ERROR_MESSAGE);
                    }
                }else{                  
                    JOptionPane.showMessageDialog(null, "Codigo de Identificação não autorizado","Codigo sem permisão",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Codigo de Identificação inválido","Codigo inválido",JOptionPane.ERROR_MESSAGE);
                JTFAutorizacao.setText("");
            }            
        }
    }//GEN-LAST:event_JTFAutorizacaoKeyPressed

