    public void buscaItensAlternativosMontagem(String codItemProducao){
        ControllerEngenhariaAlternativa eng = new ControllerEngenhariaAlternativa();
        prods = eng.buscaListaAlternativas(this.pesSaida.getCodItem(),codItemProducao);
        if(prods!=null){
            for(int i=0;i<prods.size();i++){
                DefaultTableModel model = (DefaultTableModel)jTableEngAlternativa.getModel();
                model.addRow(new Object[]{prods.get(i).getCodigo(),prods.get(i).getDescricao()} );
            }
        }        
    }           

    private void validarEngenharia() {
        okButton.setEnabled(false);
        if(!pesEntrada.getCodItem().equals(pesSaida.getCodItem())){
            for(int i=0;i<prods.size();i++){
                if(prods.get(i).getCodigo().equals(pesEntrada.getCodItem())){
                    okButton.setEnabled(true);
                    return;
                }
            }
            Object[] options = { "Sim", "Não" }; 
            int i = JOptionPane.showOptionDialog(rootPane, "O item escolhido não pertence a engenharia do produto em produção. \n"
                    + "Para que possa ser utilizado será necessária uma montagem especial. \n"
                    + "Deseja realmente realizar uma montagem especial?", 
                    "Montagem especial", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, 
                    null, options, options[0]); 
            if (i == JOptionPane.YES_OPTION) {                
                JFDAutorizarSenha auth = new JFDAutorizarSenha((Frame) this.getOwner(),false); 
                auth.setCodMaquina(codMaquina);
                auth.setMetragem(metragem);
                auth.setLote(lote);
                auth.setVisible(true);                
                if(auth.getReturnStatus()==1){
                    System.out.println("Logado com sucesso!");
                    okButton.setEnabled(true);
                }else{
                    System.out.println("Falha no login");
                }                
            }else{
                jTFNumeroPesagem.setText("");
            }
        }else{
            okButton.setEnabled(true);
        }
    }
